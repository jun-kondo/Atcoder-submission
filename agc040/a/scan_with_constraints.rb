#!/usr/bin/env ruby
# frozen_string_literal: true

# 入力を受け取る
# 例: S が "><<" のように与えられる
S = gets.chomp

# 比較記号の個数(Sの長さ)から、必要な配列のサイズを計算
# 例: S.length が 3 の場合、配列 a のサイズは 4 となる
n = S.length + 1

# 配列 a を初期化。すべての値を 0 に設定
# この配列には各要素の値が格納され、条件に基づいて調整される
a = Array.new(n, 0)

# 左から右にスキャンして `<` 条件を満たすように値を設定
# S[i] == '<' の場合、a[i+1] が a[i] よりも大きくなるようにする
(0...n - 1).each do |i|
  # S[i] が '<' の場合、a[i + 1] = a[i] + 1 に設定
  # すなわち、a[i + 1] は左隣の値 a[i] より 1 大きい
  a[i + 1] = a[i] + 1 if S[i] == '<'
end

# 右から左にスキャンして `>` 条件を満たすように値を調整
# S[i] == '>' の場合、a[i] が a[i+1] よりも大きくなるようにする
(n - 2).downto(0) do |i|
  # a[i] が a[i+1] より大きくなるには、a[i] = a[i+1] + 1 にする必要がある
  # ただし、すでに a[i] の値が大きい場合はそのまま維持する
  a[i] = [a[i], a[i + 1] + 1].max if S[i] == '>'
end

# 配列 a の値をすべて合計して出力
# これは問題の最終的な求める答え(スコアの合計など)になる
puts a.sum
